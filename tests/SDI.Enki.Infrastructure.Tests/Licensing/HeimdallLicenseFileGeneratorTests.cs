        return new Calibration(
            toolId:          toolId,
            serialNumber:    serial,
            calibrationDate: DateTimeOffset.UtcNow,
            payloadJson:     payload)
        {
            Id                = calId,
            CalibratedBy      = "test",
            MagnetometerCount = 3,
        };
    }
}
